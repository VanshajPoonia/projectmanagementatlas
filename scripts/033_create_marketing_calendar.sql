-- Creates a sheet-shaped posting calendar plus per-user completion checks.
-- Run this after users/profiles exist; it assigns the imported schedule to Kayla.

BEGIN;

CREATE TABLE IF NOT EXISTS public.marketing_calendar_items (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  assigned_to UUID REFERENCES public.profiles(id) ON DELETE SET NULL,
  source_sheet TEXT NOT NULL,
  source_row INTEGER NOT NULL,
  source_column TEXT NOT NULL,
  date DATE NOT NULL,
  day_label TEXT NOT NULL,
  section TEXT NOT NULL CHECK (section IN ('SRG', 'AGC', 'BOTH')),
  channel TEXT NOT NULL,
  content TEXT NOT NULL,
  is_highlighted BOOLEAN NOT NULL DEFAULT FALSE,
  position INTEGER NOT NULL DEFAULT 0,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
  updated_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

CREATE UNIQUE INDEX IF NOT EXISTS idx_marketing_calendar_items_source
  ON public.marketing_calendar_items(source_sheet, source_row, source_column);
CREATE INDEX IF NOT EXISTS idx_marketing_calendar_items_assigned_to
  ON public.marketing_calendar_items(assigned_to);
CREATE INDEX IF NOT EXISTS idx_marketing_calendar_items_date
  ON public.marketing_calendar_items(date);
CREATE INDEX IF NOT EXISTS idx_marketing_calendar_items_section
  ON public.marketing_calendar_items(section);

CREATE TABLE IF NOT EXISTS public.marketing_calendar_checks (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  item_id UUID NOT NULL REFERENCES public.marketing_calendar_items(id) ON DELETE CASCADE,
  user_id UUID NOT NULL REFERENCES public.profiles(id) ON DELETE CASCADE,
  checked_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT NOW(),
  created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

CREATE UNIQUE INDEX IF NOT EXISTS idx_marketing_calendar_checks_unique
  ON public.marketing_calendar_checks(item_id, user_id);
CREATE INDEX IF NOT EXISTS idx_marketing_calendar_checks_user_id
  ON public.marketing_calendar_checks(user_id);
CREATE INDEX IF NOT EXISTS idx_marketing_calendar_checks_item_id
  ON public.marketing_calendar_checks(item_id);

ALTER TABLE public.marketing_calendar_items ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.marketing_calendar_checks ENABLE ROW LEVEL SECURITY;

GRANT SELECT, INSERT, UPDATE, DELETE ON public.marketing_calendar_items TO authenticated;
GRANT SELECT, INSERT, UPDATE, DELETE ON public.marketing_calendar_checks TO authenticated;

DROP POLICY IF EXISTS "Users view assigned marketing calendar items" ON public.marketing_calendar_items;
CREATE POLICY "Users view assigned marketing calendar items"
  ON public.marketing_calendar_items FOR SELECT
  TO authenticated
  USING (
    assigned_to = auth.uid()
    OR EXISTS (SELECT 1 FROM public.profiles WHERE id = auth.uid() AND role = 'admin')
  );

DROP POLICY IF EXISTS "Admins manage marketing calendar items" ON public.marketing_calendar_items;
CREATE POLICY "Admins manage marketing calendar items"
  ON public.marketing_calendar_items FOR ALL
  TO authenticated
  USING (EXISTS (SELECT 1 FROM public.profiles WHERE id = auth.uid() AND role = 'admin'))
  WITH CHECK (EXISTS (SELECT 1 FROM public.profiles WHERE id = auth.uid() AND role = 'admin'));

DROP POLICY IF EXISTS "Users view own marketing calendar checks" ON public.marketing_calendar_checks;
CREATE POLICY "Users view own marketing calendar checks"
  ON public.marketing_calendar_checks FOR SELECT
  TO authenticated
  USING (
    user_id = auth.uid()
    OR EXISTS (SELECT 1 FROM public.profiles WHERE id = auth.uid() AND role = 'admin')
  );

DROP POLICY IF EXISTS "Users create own marketing calendar checks" ON public.marketing_calendar_checks;
CREATE POLICY "Users create own marketing calendar checks"
  ON public.marketing_calendar_checks FOR INSERT
  TO authenticated
  WITH CHECK (
    user_id = auth.uid()
    AND EXISTS (
      SELECT 1
      FROM public.marketing_calendar_items item
      WHERE item.id = item_id
        AND item.assigned_to = auth.uid()
    )
  );

DROP POLICY IF EXISTS "Users update own marketing calendar checks" ON public.marketing_calendar_checks;
CREATE POLICY "Users update own marketing calendar checks"
  ON public.marketing_calendar_checks FOR UPDATE
  TO authenticated
  USING (user_id = auth.uid())
  WITH CHECK (
    user_id = auth.uid()
    AND EXISTS (
      SELECT 1
      FROM public.marketing_calendar_items item
      WHERE item.id = item_id
        AND item.assigned_to = auth.uid()
    )
  );

DROP POLICY IF EXISTS "Users delete own marketing calendar checks" ON public.marketing_calendar_checks;
CREATE POLICY "Users delete own marketing calendar checks"
  ON public.marketing_calendar_checks FOR DELETE
  TO authenticated
  USING (user_id = auth.uid());

DROP POLICY IF EXISTS "Admins manage marketing calendar checks" ON public.marketing_calendar_checks;
CREATE POLICY "Admins manage marketing calendar checks"
